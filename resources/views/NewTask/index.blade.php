    <script>
        $(document).ready(function() {

            ajaxRequest();

            function ajaxRequest() {
                $.ajax({
                    url: '{{route("selection")}}',
                    type: 'GET',
                    data: {
                        'from': $('#select_from').val(),
                    },
                    success: function(data) {
                        $('#selection').html(data.data);
                    }
                });
            }

            $(document).on('change', '.select_from', function() {
                ajaxRequest();
            });
        });
